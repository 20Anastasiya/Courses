answ = input('Вывести симметричную разность? (yes) В противном случае будет выведена обычная разность. ')
_set1 = {1, 8, 6, 7, 'y', 10, 'g'}
_set2 = {8, 6, 4, 0, 'p', (1, 5)}
_set3 = {'y', 3, 5, 2, '19'}
if answ.lower() == 'yes':
    print(_set1 ^ _set2 ^ _set3)
else:
    print(_set1 - _set2 - _set2)
